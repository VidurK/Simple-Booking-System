    /*
     * Next piece of code translates the numeric date to the full-name date.
     */
    public String calanderDayMonth(int m,int d,int y) {
        // variables
        int days;	// number of days elapsed from a known reference date.
        int dayOfWeek; //0 for Sat. 1 for Sunday ... 6 For fri.
        String monthName;
        String dayName;
        
        // code
        if ((m==1)||(m==2)) {
            days=365*y+d+31*(m-1)+(y-1)/4-3*((y-1)/100+1)/4;
        }
        else {
            days=365*y+d+31*(m-1)- (int)(0.4*m+2.3) + (y/4)-3*(y/100+1)/4;
        }
        if ((m==1)) {
            monthName="January";
        }
        else if ((m==2)) {
            monthName="February";
        }
        else if ((m==3)) {
            monthName="March";
        }
        else if ((m==4)) {
            monthName="April";
        }
        else if ((m==5)) {
            monthName="May";
        }
        else if ((m==6)) {
            monthName="June";
        }
        else if ((m==7)) {
            monthName="July";
        }
        else if ((m==8)) {
            monthName="August";
        }
        else if ((m==9)) {
            monthName="September";
        }
        else if ((m==10)) {
            monthName="October";
        }
        else if ((m==11)) {
            monthName="November";
        }
        else if ((m==12)) {
            monthName="December";
        }
        else {
            monthName="Invalid Month!";
        }
        dayOfWeek=days%7-1;
        if ((dayOfWeek==0)) {
            dayName="Sunday";
        }
        else if ((dayOfWeek==1)) {
            dayName="Monday";
        }
        else if ((dayOfWeek==2)) {
            dayName="Tuesday";
        }
        else if ((dayOfWeek==3)) {
            dayName="Wednesday";
        }
        else if ((dayOfWeek==4)) {
            dayName="Thursday";
        }
        else if ((dayOfWeek==5)) {
            dayName="Friday";
        }
        else if ((dayOfWeek==6 || dayOfWeek==-1)) {
            dayName="Saturday";
        }
        else  {
            dayName="Invalid Day";
        }
        return (dayName+", "+monthName);
    }
