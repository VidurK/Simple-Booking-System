    /*
     * This next method checks the booking to see if all the dates are compatiable with
     * the bookings in the current record. If avaiable it books it coorespondingly.
     */
    public Boolean checkBooking(Booking[] b, int bookCampsiteNumber,int bookings) {
        String arrivalDate = txtArrival.getText();
        String departDate = txtDeparture.getText();
        String[] dates = amountOfDays(arrivalDate,departDate); //Uses the amount of days method to receive the amount of days that are booked.
        // a nested loop to check for every booking and all dates inside of the array.
        for (int i=0;i<bookings;i++) {
            for (int j=0;j<dates.length;j++) {
                String[] dates2 = amountOfDays(b[i].arrivalDate,b[i].departDate);
                for (int k=0; k<dates2.length;k++) {
                    if ((dates[j]).equals(dates2[k])) {
                        return true;// any at least one date is caught in this loop it returns true so there is no ability what so ever of double-booking.
                    }
                }
            }
        }
        return false;
    }
